$(function () {
    $(".usernameLiImg").mouseenter(function () {
        $(".hideImg").css("display", "list-item");
    });
    $(".hideImg").mouseenter(function () {
        $(".hideImg").css("display", "list-item");
    });
    $(".usernameLiImg").mouseleave(function () {
        $(".hideImg").css("display", "none");
    });
    $(".hideImg").mouseleave(function () {
        $(".hideImg").css("display", "none");
    });


})


